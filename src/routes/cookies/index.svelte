    <h2>Website-ul nostru folosește cookie-uri</h2>
    <h5>Ce este un Cookie?</h5>
    <p>
      Cookie-ul este un fișier care conține un identificator, care este trimis
      de un server web către un browser și care este stocat de acel browser.
      Identificatorul este apoi trimis înapoi la server de fiecare dată când
      browser-ul solicită o pagină de la acesta.
    </p>
    <p>
      Cookie-urile pot fi cookie-uri „persistente” sau cookie-uri „de sesiune”:
      cookie-ul persistent va fi stocat de către browser și va rămâne valabil
      până la data stabilită de expirare, cu excepția cazului în care acesta
      este șters înainte de data de expirare.
    </p>
    <p>
      Pe de altă parte, cookie-ul de sesiune va expira la sfârșitul sesiunii
      utilizatorului, la închiderea browser-ului.
    </p>
    <p>
      Cookie-urile nu conțin de obicei informații care identifică personal un
      utilizator, dar informațiile personale pe care le stocăm despre dvs. pot
      fi legate de informațiile stocate și obținute prin cookie-uri.
    </p>

    <p>
      Numele cookie-urilor pe care le folosim pe website-ul nostru și scopurile
      pentru care sunt utilizate acestea pot fi găsite mai jos:
    </p>
    <p>
      Folosim Google Analytics și Adwords pe website-ul nostru pentru a
      recunoaște un computer atunci când un utilizator vizitează website-ul,
      urmărește utilizatorii cât timp navighează pe website, îmbunătățește
      capacitatea de utilizare a website-ului, analizează utilizarea
      website-ului, administrează website-ul, previne fraudele și îmbunătățește
      securitatea website-ului.
    </p>
    <h5>
      Majoritatea browser-elor vă permit să refuzați să acceptați cookie-uri –
      de exemplu:
    </h5>
    <ul>
      <li>
        în Internet Explorer puteți bloca cookie-urile utilizând suprascrierea
        setărilor de manipulare a cookie-urilor, disponibile printr-un clic pe
        „Instrumente”, „Opțiuni de Internet”, „Confidențialitate”, apoi
        „Avansat”
      </li>
      <li>
        în Firefox puteți bloca toate cookie-urile făcând clic pe „Instrumente”,
        „Opțiuni”, „Confidențialitate”, selectând „Utilizați setări
        personalizate pentru istoric” din meniul derulant și debifând „Acceptați
        cookie-uri de la website-uri”
      </li>
      <li>
        în Chrome, puteți bloca toate cookie-urile accesând meniul
        „Personalizare și control”, făcând clic pe „Setări”, „Afișați setările
        avansate” și pe „Setări de conținut”, apoi selectând „Blocați site-urile
